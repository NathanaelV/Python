# Calcular a média aritimética de duas notas

n1 = float(input('Primeira nota: '))
n2 = float(input('Segunda nota: '))
média = (n1 + n2)/2  # Python aceita acento como variável
print('A média do aluno é: {:.2f}.'.format(média))

